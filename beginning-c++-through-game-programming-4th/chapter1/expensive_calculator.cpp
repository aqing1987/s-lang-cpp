// Expensive Calculator
// Demonstrates built-in arithmetic operators
#include <iostream>

using namespace std;

int main(int argc, char *argv[]) {
  cout << "7 + 3 = " << 7 + 3 << endl;
  cout << "7 - 3 = " << 7 - 3 << endl;
  cout << "7 * 3 = " << 7 * 3 << endl;
  cout << "7 / 3 = " << 7 / 3 << endl;
  cout << "7.0 / 3 = " << 7.0 / 3 << endl;
  cout << "7 % 3 = " << 7 % 3 << endl;
  cout << "7 + 3 * 5 = " << 7 + 3*5 << endl;
  cout << "(7+3) * 5 = " << (7+3)*5 << endl;
  return 0;
}
